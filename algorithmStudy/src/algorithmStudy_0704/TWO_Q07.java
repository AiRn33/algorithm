package algorithmStudy_0704;

import java.util.Scanner;

public class TWO_Q07 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		int no; // 변환하는 정수
		int cd; // 기수
		int dno; // 변환 후의 자릿 수
		int retry; // 다시 한 번
		char cno[] = new char[32]; // 변환 후 각 자리의 숫자를 넣어두는 문자의 배열

		do {

			do {
				System.out.print("변환 하는 음이 아닌 정수 : ");
				no = sc.nextInt();
			} while (no < 0);

			do {
				System.out.print("어떤 진수로 변환할까요? (2~36) : ");
				cd = sc.nextInt();
			} while (cd < 2 || cd > 36);

			dno = cardConvR(no, cd, cno);

			System.out.print(cd + "진수로는 ");

			for (int i = dno - 1; i >= 0; i--) {
				System.out.print(cno[i]);
			}
			System.out.print("입니다..");

			System.out.println("한번 더 할까요  /  1.예 , 2.아니오");
			retry = sc.nextInt();

		} while (retry == 1);
	}

	static int cardConvR(int no, int cd, char cno[]) {

		int digits = 0;
		String dchar = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ";
		System.out.println("2 |     " + no);
		System.out.println("  +------------");
		do {
			int num = no% cd;

			System.out.println("2 |     " + no/2 +"..."+ num);
			System.out.println("  +------------");
			cno[digits++] = dchar.charAt(no % cd);
			no /= cd;
		} while (no != 0);


		return digits;
	}
}
